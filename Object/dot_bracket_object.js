const user = {
    name : "Gulshid Zada",
    "age" : 23
};


console.log(user.name); // dot notation
console.log(user["age"]); // bracket notation

/*
Lecture # 51
    Topic : Dot vs Bracket Notation in JavaScript

    Dot vs Bracket Notation:
        * Dot: simple keys
            name , age 
        * Bracket: special keys / dynamic keys
            "name", "age"
*/
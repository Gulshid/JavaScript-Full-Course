let fruits = ["Apple", "Mango", "Orange", "Banana"];
console.log(fruits);
console.log("=============");

// Array Length
console.log("The length of Fruits Array :",fruits.length);
console.log("=============");

// push() Method 
fruits.push("Graps");
console.log(fruits);
console.log("The length of Fruits Array :",fruits.length);
console.log("=============");

// pop() Method 
fruits.pop();
console.log(fruits);
console.log("The length of Fruits Array :",fruits.length);

let numbers = [1, 2, 3, 4, 5];

console.log("Number :", numbers);
console.log("==============");

// shift() → remove first element
numbers.shift();
console.log("Number :", numbers);
console.log("==============");

// unshift() → add element at beginning
numbers.unshift(1);
console.log("Number :", numbers);

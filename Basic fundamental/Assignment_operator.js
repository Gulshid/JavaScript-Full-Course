let a = 2; // a = 2
console.log("= :", a);

a += 3; // a = 5
console.log("+= :", a);

a -= 4; // a = 1
console.log("-= :", a);

a *= 2; // a = 2
console.log("*= :", a);

a /= 2; // a = 1
console.log("/= :", a);

a %= 3; // a = 1
console.log("%= :", a);

a **= 2; // a = 1
console.log("**= :", a);



console.log(2 == 6); // false

console.log(2 === "2"); // false

console.log(3 != 2); // true

console.log(10 !== "10"); // true

console.log(10 > 5); // true

console.log(10 < 5); // false

console.log(2 >= 4); // true

console.log(3 <= 5); // true